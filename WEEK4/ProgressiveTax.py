def main():
    """ProgressiveTax"""
    result = 0
    income = int(input())
    if 0 < income <= 150000:
        result = 0
    elif 150000 < income <= 300000:
        result += (income-150000)*(5/100)
    elif 300000 < income <= 500000:
        result += (income-300000)*(10/100)+(150000*(5/100))
    elif 500000 < income <= 750000:
        result += (income-500000)*(15/100)+(200000*(10/100))+(150000*(5/100))
    elif 750000 < income <= 1000000:
        result += (income - 750000) * (20 / 100) + (250000 * (15 / 100)) + \
                  (200000 * (10 / 100)) + (150000 * (5 / 100))
    elif 1000000 < income <= 2000000:
        result += (income - 1000000) * (25 / 100) + (250000 * (20 / 100)) + \
                  (250000 * (15 / 100)) + (200000 * (10 / 100)) + (150000 * (5 / 100))
    elif 2000000 < income <= 4000000:
        result += (income - 2000000) * (30 / 100) + (1000000 * (25 / 100)) + \
                  (250000 * (20 / 100)) + (250000 * (15 / 100)) + (200000 * (10 / 100)) + \
                  (150000 * (5 / 100))
    elif income > 4000000:
        result += (income - 4000000) * (35 / 100) + (2000000 * (30 / 100)) + \
                  (1000000 * (25 / 100)) + (250000 * (20 / 100)) + (250000 * (15 / 100)) + \
                  (200000 * (10 / 100)) + (150000 * (5 / 100))
    print(int(result))
main()
