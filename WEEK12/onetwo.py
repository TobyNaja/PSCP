"""Seconds Converter"""
# รับค่ามาเป็นจำนวนวินาที
s = int(input())
# คำนวณจำนวนวัน
days = s//86400
s = s % 86400
print(s)
# คำนวณจำนวนชั่วโมง
hours = s//3600
s = s % 3600
# คำนวณจำนวนนาที
minutes = s//60
# คำนวณจำนวนวินาทีที่เหลือ
seconds = s%60
# แสดงผลลัพธ์ โดยให้ชั่วโมง นาที และวินาทีมี 2 หลักเสมอ
print(f"{days}:{hours:02}:{minutes:02}:{seconds:02}")
